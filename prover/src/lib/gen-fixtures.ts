console.log(`pi_b: ${[...pi_b_bytes].map(b => b.toString(16).padStart(2,'0')).join('')}`);
console.log(`pi_c: ${[...pi_c_bytes].map(b => b.toString(16).padStart(2,'0')).join('')}`);
